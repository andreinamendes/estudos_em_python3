"""
Mostrar valores em tela
  - O Python define como função para exibir valores na tela como print().
"""

print('Hello World') # Forma básica, já define a quebra de linha automática
print('Hello', 'World', sep='-') # Definindo que os valores serão separados por espaços
print('Hello', 'World', sep='-', end='###') # Define o final do print como '' e retira a quebra de linha padrão