print(type(str)) # Type printa o tipo de um objeto

print(str(23)) # Casting de tipos, vai realizar a conversão, a mudança vai de acordo com o nome da função

# Nome: String
print("Andreina Mendes", type("Andreina Mendes"))

# Idade: Int
print(21, type(21))

# Altura: Float
print(1.64, type(1.64))

# Maior de idade: Bool
print(True, type(True))